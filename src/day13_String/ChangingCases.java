package day13_String;

public class ChangingCases {

	public static void main(String[] args) {
		
		String str = "Hello";
		System.out.println(str);
		
		str = str.toLowerCase();
		System.out.println(str);
		
		str = str.toUpperCase();
		System.out.println(str);
		
		String str2 = "ake"; //AKE
		System.out.println(str2);
		
		str2 = str2.toUpperCase();
		System.out.println(str2);

	}

}
