package day05_operators;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a = 9;
		
		
		a += 5; // a = a +5;
		
		System.out.println(a); //14
		
		a += 5; // a = a + 5;
		
		System.out.println(a); //9
		
		a -= 10; // a = a - 10
		
		a /= 3; // a = a / 3;
		
		System.out.println(a); //6
		
		a *= 2; // a = a *2
		
		System.out.println(a); //12
		
		

	}

}
